public class PTTT {
    public int maPTTT;
    public String tenPTTT;

    public PTTT() {
    }

    public PTTT(int maPTTT, String tenPTTT) {
        this.maPTTT = maPTTT;
        this.tenPTTT = tenPTTT;
    }

    public int getMaPTTT() {
        return maPTTT;
    }

    public void setMaPTTT(int maPTTT) {
        this.maPTTT = maPTTT;
    }

    public String getTenPTTT() {
        return tenPTTT;
    }

    public void setTenPTTT(String tenPTTT) {
        this.tenPTTT = tenPTTT;
    }
    
    
    
}
